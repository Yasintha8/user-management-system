import mongoose from "mongoose";

const registerUserSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
});

const RegisterUser = mongoose.model("RegisterUser", registerUserSchema);
export default RegisterUser;